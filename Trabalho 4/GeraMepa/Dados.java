import java.util.*;

public class Dados {
	String nome;
	int nivel;
	String categoria;
	String endereco;
	int parametros;

	public Dados(String nome, int nivel, String categoria, String endereco, int parametros){
		this.nome = nome;
		this.nivel = nivel;
		this.categoria = categoria;
		this.endereco = endereco;
		this.parametros = parametros;
	}
}
